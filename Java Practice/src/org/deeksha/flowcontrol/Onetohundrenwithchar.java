package org.deeksha.flowcontrol;

public class Onetohundrenwithchar {
	public static void main(String[] args) {
		int s ='s'/'s';
		String sr ="abcdefghtj";
		for (int i=s;i<='d';i++)
		{
			System.out.println(i);
		}
		for(int i =s;i<=sr.length()*sr.length();i++)
		{
			System.out.println(i);
		}
	}

}
